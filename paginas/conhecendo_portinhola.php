<tr><td background="imagens/fundo_azul2.jpg"><a class="menu">
<br> Permite a passagem do fluído em apenas um sentido, fechando-se automaticamente por diferenças de pressões, exercidas pelo fluído em conseqüência do próprio escoamento, se houver tendência à inversão no sentido do fluxo. São portando, válvulas de operação automática.<br><br></a></td></tr>
<tr><td background="imagens/fundo_titulo.jpg"><a class="titulo_branco2">&nbsp;Onde posso utiliza-la?</a></td></tr>
<tr><td background="imagens/fundo_azul2.jpg"><a class="menu">
<br> Se você pretende impedir em uma determinada linha qualquer possibilidade de retorno de fluído, esta é a válvula ideal para a sua aplicação!<br><br>

Veja alguns exemplos de onde são utilizadas:<br><br>

* Linhas de  recalque de bombas (imediatamente após uma bomba) quando houver mais de uma bomba em paralelo descarregando no mesmo tronco. As válvulas de retenção servirão nesse caso para evitar a possibilidade da ação de uma bomba que estiver operando sobre outras bombas que estiverem paradas.<br><br>

* Linha de recalque de uma bomba para um reservatório elevado. A válvula evitará o retorno do líquido no caso de ocorrer uma paralisação súbita no funcionamento da bomba.<br><br>

* Extremidade livre da linha de sucção de uma bomba, no caso de sistemas com sucção não afogada; a válvula de retenção, denominada "válvula de pé", deve estar suficientemente mergulhada no líquido do reservatório de sucção, e servirá para manter a escorva na tubulação e na própria bomba, isto é, evitar o seu esvaziamento, durante o tempo em que a bomba estiver parada.<br><br>

</a></td></tr>
<tr><td background="imagens/fundo_titulo.jpg"><a class="titulo_branco2">&nbsp;Quais as condições para utilizar esta válvula?</a></td></tr>
<tr><td background="imagens/fundo_azul2.jpg"><a class="menu">
<br>
* As válvulas do tipo retenção portinhola podem ser utilizadas em qualquer posição (caso necessite de uma válvula retenção apenas para uso na horizontal, recomendamos a do tipo pistão).<br><br>

* Esta válvula também é indicada para trabalhar com gases e vapores.<br><br>

* Não devem ser utilizadas para fluídos que deixem sedimentos ou depósitos sólidos.<br><br>

* Esta válvula causa uma perda de causa considerável, porém menor que a retenção pistão.<br><br></td></tr>
